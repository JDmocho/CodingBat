# funkcja diff21, zwraca roznice 21 - n


def diff21(n):
    return 21 - n

print ('Wywolujemy diff21(19)')
wynik = diff21(19)
print ('Wynik funkcji to', wynik)

print ('Wywolujemy diff21(10)')
wynik = diff21(10)
print ('Wynik funkcji to', wynik)

print ('Wywolujemy diff21(21)')
wynik = diff21(21)
print ('Wynik funkcji to', wynik)
